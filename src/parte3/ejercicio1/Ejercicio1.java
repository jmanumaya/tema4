package parte3.ejercicio1;

/**
 * Clase que proporciona métodos para sumar números enteros y decimales.
 */
public class Ejercicio1 {

    /**
     * Suma dos números enteros.
     *
     * @param num1 El primer número entero.
     * @param num2 El segundo número entero.
     * @return La suma de los dos números enteros.
     */
    public int suma(int num1, int num2) {
        int resultado = num1 + num2;
        return resultado;
    }

    /**
     * Suma dos números de tipo double.
     *
     * @param num1 El primer número decimal.
     * @param num2 El segundo número decimal.
     * @return La suma de los dos números de tipo double.
     */
    public double suma(double num1, double num2) {
        double resultado = num1 + num2;
        return resultado;
    }
}
